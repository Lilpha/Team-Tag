import java.util.*;
public class Bac1550 {
	public static void main(String[]args) {
		//https://www.acmicpc.net/submit/1550
		Scanner sc = new Scanner(System.in);
		String num = sc.next();//String형태로 받아서 ParseInt사용예정
		
		int number = Integer.parseInt(num,16); //number에 num을 정수형 16진수변환
		System.out.println(number);
		
		
	}
}
